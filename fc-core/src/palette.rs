//! NES system palette (64 colors) and `.pal` file loading.

use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub struct Rgb {
    pub r: u8,
    pub g: u8,
    pub b: u8,
}

impl Rgb {
    pub const fn new(r: u8, g: u8, b: u8) -> Self {
        Rgb { r, g, b }
    }
}

/// A 64-entry system palette.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Palette {
    pub colors: Vec<Rgb>,
}

impl Default for Palette {
    fn default() -> Self {
        Palette {
            colors: DEFAULT_PALETTE.to_vec(),
        }
    }
}

impl Palette {
    /// Load a `.pal` file: 192 bytes (64×RGB) or 1536 bytes (512×RGB, takes
    /// the first 64 for the base emphasis level).
    pub fn from_pal(data: &[u8]) -> Option<Palette> {
        if data.len() < 192 {
            return None;
        }
        let colors = data[..192]
            .chunks_exact(3)
            .map(|c| Rgb::new(c[0], c[1], c[2]))
            .collect();
        Some(Palette { colors })
    }
}

